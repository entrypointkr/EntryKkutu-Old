public class Profile extends GameObject {
    private String token;
    private String type;
    private String id;
    private String name;
    private String image;
    private String sid;

    public String getToken() {
        return token;
    }

    public String getType() {
        return type;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getSid() {
        return sid;
    }
}
